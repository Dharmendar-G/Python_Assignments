# Relational Operators (<,<=,>,>=)

# Lessthan operator (<)
print(8 < 10)

# Greaterthan operator(>)
print(6 > 2)

# Lessthan or equal (<=)
print(8<=8)
print(6<=8)

# Greaterthan or equal (>=)
print(10>=10)
print(20>=15)

